#include <stdio.h>
#include <stdlib.h>
/* variables are data holder. we can save value in variable. variable contains a data type and name of the variable.
Defining and initializing could be done at same time. int , float , char and double are basic data type in C. */
int main()
{
    int a=21;
    float b=3.14;
    char c='a';
    double d=3.2145666884664;
    printf("%d \n",a);
    printf("%f\n",b);
    printf("%c\n",c);
    printf("%lf\n",d);
    return 0;
}
